# membership operator 
club_member=[
    "Almas",
    "Sakib",
    "Tamim",
    "Nipun",
    "Bakkar"
]

new_person = "Rubel"
dcision =new_person in club_member
print(dcision)
dcision= new_person not in club_member
print(dcision)

#direct use 
print("Almas" in club_member)
print("Sakib" in club_member)
print("tamim" in club_member) # case sensitive